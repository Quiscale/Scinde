package scinde.controller;

public class ActionEnd {

	// ////////////////////////////////////////////////////////////////////////
	// Attributes
	// ////////////////////////////////////////////////////////////////////////

	private GameAction action;
	
	// ////////////////////////////////////////////////////////////////////////
	// Constructors
	// ////////////////////////////////////////////////////////////////////////

	public ActionEnd() {
		super();
		
		action = null;
	}
	
	// ////////////////////////////////////////////////////////////////////////
	// Methods
	// ////////////////////////////////////////////////////////////////////////

	public void trigger() {
		if(this.action != null) {
			this.action.handle();
		}
	}
	
	public void setOnEnd(GameAction action) {
		this.action = action;
	}
	
	// ////////////////////////////////////////////////////////////////////////
	// Overrides
	// ////////////////////////////////////////////////////////////////////////
}
